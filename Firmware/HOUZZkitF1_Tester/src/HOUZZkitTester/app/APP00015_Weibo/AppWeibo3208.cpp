bool AppWeiboLayer3208::initLayer()
{
     _layerStyle = LayerStyleIconNum3208::create();
    _layerStyle->initWithData(icon_weibo+2,GetUint16(icon_weibo),this->getData(),this);
    this->addChild(_layerStyle);
    return true;
}


void AppWeiboLayer3208::printAction(SpriteCanvas* canvas)
{
    AppDataWeibo* data = this->getData();
    if (data->isCountFormat)
    {
        String countStr = NumberFormat(data->fansCount,data->numberSize);
        canvas->printf("%s",countStr.c_str());
    }else
    {
        canvas->printf("%d",data->fansCount);
    }
}


