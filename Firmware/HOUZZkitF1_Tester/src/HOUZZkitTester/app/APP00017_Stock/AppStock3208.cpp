bool AppStockLayer3208::initLayer()
{
    _layerStyle = LayerStyleIconNum3208::create();
    uint8_t lenght = this->getData()->symbolName.length();
    _layerStyle->initWithData(icon_stock+2,GetUint16(icon_stock),this->getData(),this,MAX(lenght + 15,48));
    this->addChild(_layerStyle);
    return true;
}

void AppStockLayer3208::printAction(SpriteCanvas* canvas)
{
    if(this->getData()->isShowStockCode)
    {
        canvas->printf("%s %.2f",this->getData()->symbolName.c_str(),this->getData()->price);
    }else{
        canvas->printf("%.2f",this->getData()->price);
    }
}

